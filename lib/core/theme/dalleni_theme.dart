  static ThemeData _buildTheme({
    required Locale locale,
    required ColorScheme colorScheme,
    required Color scaffoldBackgroundColor,
    required Color cardColor,
    required Color inputFillColor,
  }) {
    final fontFamily = locale.languageCode == 'ar'
        ? arabicFontFamily
        : englishFontFamily;

    return ThemeData(
      brightness: colorScheme.brightness,
      useMaterial3: true,
      colorScheme: colorScheme,
      scaffoldBackgroundColor: scaffoldBackgroundColor,
      fontFamily: fontFamily,
      appBarTheme: AppBarTheme(
        backgroundColor: cardColor,
        foregroundColor: colorScheme.onSurface,
        elevation: 0,
      ),
      cardTheme: CardThemeData(
        color: cardColor,
        elevation: 0,
        shape: const RoundedRectangleBorder(
          borderRadius: BorderRadius.all(Radius.circular(20)),
        ),
      ),
      inputDecorationTheme: InputDecorationTheme(
        filled: true,
        fillColor: inputFillColor,
        border: OutlineInputBorder(
          borderRadius: BorderRadius.circular(16),
          borderSide: BorderSide.none,
        ),
        enabledBorder: OutlineInputBorder(
          borderRadius: BorderRadius.circular(16),
          borderSide: BorderSide.none,
        ),
        focusedBorder: OutlineInputBorder(
          borderRadius: BorderRadius.circular(16),
          borderSide: BorderSide(color: colorScheme.primary, width: 1.5),
        ),
      ),
      elevatedButtonTheme: ElevatedButtonThemeData(
        style: ElevatedButton.styleFrom(
          backgroundColor: colorScheme.primary,
          foregroundColor: colorScheme.onPrimary,
          minimumSize: const Size.fromHeight(56),
          shape: RoundedRectangleBorder(
            borderRadius: BorderRadius.circular(16),
          ),
        ),
      ),
      outlinedButtonTheme: OutlinedButtonThemeData(
        style: OutlinedButton.styleFrom(
          foregroundColor: colorScheme.onSurface,
          minimumSize: const Size.fromHeight(56),
          side: BorderSide(color: colorScheme.outlineVariant),
          shape: RoundedRectangleBorder(
            borderRadius: BorderRadius.circular(16),
          ),
        ),
      ),
      textTheme: _createTextTheme(
        displayColor: colorScheme.onSurface,
        bodyColor: colorScheme.onSurfaceVariant,
        labelColor: colorScheme.onSurfaceVariant,
        fontFamily: fontFamily,
      ),
      dividerColor: colorScheme.outlineVariant,
    );
  }

  static TextTheme _createTextTheme({
    required Color displayColor,
    required Color bodyColor,
    required Color labelColor,
    required String fontFamily,
  }) {
    return TextTheme(
      displayLarge: TextStyle(color: displayColor, fontFamily: fontFamily),
      displayMedium: TextStyle(color: displayColor, fontFamily: fontFamily),
      displaySmall: TextStyle(color: displayColor, fontFamily: fontFamily),
      headlineLarge: TextStyle(color: displayColor, fontFamily: fontFamily),
      headlineMedium: TextStyle(color: displayColor, fontFamily: fontFamily),
      headlineSmall: TextStyle(color: displayColor, fontFamily: fontFamily),
      titleLarge: TextStyle(color: displayColor, fontFamily: fontFamily),
      titleMedium: TextStyle(color: displayColor, fontFamily: fontFamily),
      titleSmall: TextStyle(color: displayColor, fontFamily: fontFamily),
      bodyLarge: TextStyle(color: bodyColor, fontFamily: fontFamily),
      bodyMedium: TextStyle(color: bodyColor, fontFamily: fontFamily),
      bodySmall: TextStyle(color: bodyColor, fontFamily: fontFamily),
      labelLarge: TextStyle(color: labelColor, fontFamily: fontFamily),
      labelMedium: TextStyle(color: labelColor, fontFamily: fontFamily),
      labelSmall: TextStyle(color: labelColor, fontFamily: fontFamily),
    );
  }
